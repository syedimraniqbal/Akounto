package com.akounto.accountingsoftware.Data;

public class RadioData {
    private String itemtypeDescription;
    private int itemtype;
    private String unittypeDescription;
    private int unittype;
    private String pricetypeDescription;
    private int pricetype;

    public String getItemtypeDescription() {
        return itemtypeDescription;
    }

    public void setItemtypeDescription(String itemtypeDescription) {
        this.itemtypeDescription = itemtypeDescription;
    }

    public int getItemtype() {
        return itemtype;
    }

    public void setItemtype(int itemtype) {
        this.itemtype = itemtype;
    }

    public String getUnittypeDescription() {
        return unittypeDescription;
    }

    public void setUnittypeDescription(String unittypeDescription) {
        this.unittypeDescription = unittypeDescription;
    }

    public int getUnittype() {
        return unittype;
    }

    public void setUnittype(int unittype) {
        this.unittype = unittype;
    }

    public String getPricetypeDescription() {
        return pricetypeDescription;
    }

    public void setPricetypeDescription(String pricetypeDescription) {
        this.pricetypeDescription = pricetypeDescription;
    }

    public int getPricetype() {
        return pricetype;
    }

    public void setPricetype(int pricetype) {
        this.pricetype = pricetype;
    }
}
